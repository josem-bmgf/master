CREATE VIEW [dbo].[vInvestmentScoringReporting]
	AS 

SELECT [ID_Combined]
      ,[INV_Grantee_Vendor_Name]
      ,[INV_Type]
      ,[INV_Status]
      ,[INV_Title]
      ,[INV_Owner]
      ,[INV_Manager]
      ,[INV_Start_Date]
      ,[INV_End_Date]
      ,[INV_Level_of_Engagement]
      ,[Total_Investment_Amount]
      ,[Amt_Paid_To_Date]
      ,[INV_Description]
      ,[Managing_Team_Level_1]
      ,[Managing_Team_Level_2]
      ,[Managing_Team_Level_3]
      ,[Managing_Team_Level_4]
      ,[PMT_Expenditure_Type]
      ,[Funding_Type]
	  ,NULL AS [Performance_Against_Milestones]
      ,NULL AS [Performance_Against_Strategy]
      ,NULL AS [Reinvestment_Prospects]
      ,NULL AS [Rationale]
      ,NULL AS [Highest_Performing]
      ,NULL AS [Lowest_Performing]
      ,NULL AS [Exclude_from_Scoring]
      ,NULL AS [Investment_ID]
      ,NULL AS [Score_Date]
      ,NULL AS [Score_Year]
      ,NULL AS [User]
	  ,NULL AS [On_Behalf_Of]
      ,NULL AS [Objective]
	  ,NULL AS [Relative_Strategic_Importance]
	  ,NULL AS [Key_Results_Data]
	  ,NULL AS [Funding_Team_Input]
	  ,NULL AS [Is_Archived]
	  ,NULL AS [Is_Excluded]
      ,[Is_Excluded_From_TOI]
	  
  FROM [dbo].[Investment]
  WHERE Is_Deleted=0 	
  GO