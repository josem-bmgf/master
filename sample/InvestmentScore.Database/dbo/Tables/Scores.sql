CREATE TABLE [dbo].[Scores] (
    [ID]                             INT            IDENTITY (1, 1) NOT NULL,
    [Performance_Against_Milestones] NVARCHAR (220) NULL,
    [Performance_Against_Strategy]   NVARCHAR (220) NULL,
    [Reinvestment_Prospects]         NVARCHAR (220) NULL,
    [Rationale]                      NVARCHAR (MAX) NULL,
    [Highest_Performing]             BIT            NOT NULL,
    [Lowest_Performing]              BIT            NOT NULL,
    [Exclude_from_Scoring]           BIT            NOT NULL,
    [Investment_ID]                  INT            NULL,
    [Score_Date]                     DATETIME       NULL,
    [Score_Year]                     INT            NULL,
    [User]                           NVARCHAR (220) NULL,
    [Objective]                      NVARCHAR (220) NULL,
    [Old_Investment_ID]              INT            NULL,
    [Old_Investment_ID_MNF]          INT            NULL,
    [Scored_By_ID]                   NVARCHAR (255) NULL,
    [Created_On]                     DATETIME       NULL,
    [Created_By_ID]                  NVARCHAR (255) NULL,
    [Modified_On]                    DATETIME       NULL,
    [Modified_By_ID]                 NVARCHAR (255) NULL,
	[Relative_Strategic_Importance]	 NVARCHAR (220) NULL,
	[Key_Results_Data]				 NVARCHAR (MAX) NULL,
	[Funding_Team_Input]			 NVARCHAR (MAX) NULL,
	[Is_Archived]                    BIT            NULL,
	[Is_Excluded]                    BIT            NULL,
    CONSTRAINT [PK_Scoring] PRIMARY KEY CLUSTERED ([ID] ASC),
    CONSTRAINT [FK_Created_By_ID] FOREIGN KEY ([Created_By_ID]) REFERENCES [dbo].[User] ([ID]),
    CONSTRAINT [FK_Modified_By_ID] FOREIGN KEY ([Modified_By_ID]) REFERENCES [dbo].[User] ([ID]),
    CONSTRAINT [FK_Scored_By_ID] FOREIGN KEY ([Scored_By_ID]) REFERENCES [dbo].[User] ([ID]),
    CONSTRAINT [FK_Scoring_ToInvestment] FOREIGN KEY ([Investment_ID]) REFERENCES [dbo].[Investment] ([ID])
);

